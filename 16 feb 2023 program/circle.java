/*WAP to compute the perimeter and area of circle with radius of 6 inches*/
public class circle
{
	public static void main(String[] args)
		{
			int r=6;
			double aoc,poc;
			poc=2*3.14*r;
			System.out.println("Perimeter of a circle : "+poc);

			aoc=3.14*r*r;
			System.out.println("Area of a circle : "+aoc);
		}
}